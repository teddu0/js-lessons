// Relational or comparison operators

// > - more than
// < - less than
// >= more than equal
// <= less than equal

let result = 10 > 5;
console.log(result);

// equalit operators

let x = 1;
console.log(x == "1"); // lose comparison (сравнивает только значения)
console.log(x === "1"); // strict comparison (сравнивает значение и тип данных)
